// Program to demonstrate storing and displaying all primitive data types in Java

public class StoringAllPrimitiveDataTypes {   // Declares the public class named StoringAllPrimitiveDataTypes

    public static void main(String[] args) {  // Main method where program execution starts

        byte byteValue = 100;                 // Declares and initializes a byte variable

        short shortValue = 30000;             // Declares and initializes a short variable

        int intValue = 250000;                // Declares and initializes an int variable

        long longValue = 9876543210L;         // Declares and initializes a long variable (L indicates long literal)

        float floatValue = 45.75f;            // Declares and initializes a float variable (f indicates float literal)

        double doubleValue = 98765.4321;      // Declares and initializes a double variable

        char characterValue = 'A';            // Declares and initializes a character variable

        boolean booleanValue = true;          // Declares and initializes a boolean variable

        System.out.println("Java Primitive Data Types"); // Prints the program heading

        System.out.println("-------------------------"); // Prints a separator line

        System.out.println("byte    : " + byteValue); // Displays the value stored in the byte variable

        System.out.println("short   : " + shortValue); // Displays the value stored in the short variable

        System.out.println("int     : " + intValue); // Displays the value stored in the int variable

        System.out.println("long    : " + longValue); // Displays the value stored in the long variable

        System.out.println("float   : " + floatValue); // Displays the value stored in the float variable

        System.out.println("double  : " + doubleValue); // Displays the value stored in the double variable

        System.out.println("char    : " + characterValue); // Displays the value stored in the char variable

        System.out.println("boolean : " + booleanValue); // Displays the value stored in the boolean variable

    } // Ends the main method

} // Ends the StoringAllPrimitiveDataTypes class
