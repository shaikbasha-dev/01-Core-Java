// Sum of Two Integers Program in Java

// Define a public class named SumOfIntegers
public class SumOfIntegers {

    // Main method - JVM starts program execution from here
    public static void main(String[] args) {

        // Declare and initialize the first integer
        int firstNumber = 20;

        // Declare and initialize the second integer
        int secondNumber = 30;

        // Calculate the sum of the two integers
        int sum = firstNumber + secondNumber;

        // Display the input values
        System.out.println("First Number  : " + firstNumber);
        System.out.println("Second Number : " + secondNumber);

        // Display the calculated sum
        System.out.println("Sum           : " + sum);

    } // End of main method

} // End of SumOfIntegers class
