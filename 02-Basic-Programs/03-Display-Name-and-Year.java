// Display Name and Year Program in Java

// Define a public class named DisplayNameAndYear
public class DisplayNameAndYear {

    // Main method - JVM starts program execution from here
    public static void main(String[] args) {

        // Declare and initialize a String variable to store the programming language name
        String languageName = "Java";

        // Declare and initialize an integer variable to store the release year
        int releaseYear = 1995;

        // Display the programming language name
        System.out.println("Programming Language : " + languageName);

        // Display the release year
        System.out.println("Release Year         : " + releaseYear);

    } // End of main method

} // End of DisplayNameAndYear class
