// Basic Calculator (Hard Coding) Program in Java

// Define a public class named BasicCalculatorHardCoding
public class BasicCalculatorHardCoding {

    // Main method - JVM starts program execution from here
    public static void main(String[] args) {

        // Declare and initialize the first integer
        int firstNumber = 100;

        // Declare and initialize the second integer
        int secondNumber = 50;

        // Perform arithmetic operations
        int addition = firstNumber + secondNumber;
        int subtraction = firstNumber - secondNumber;
        int multiplication = firstNumber * secondNumber;
        int division = firstNumber / secondNumber;

        // Display the input values
        System.out.println("First Number  : " + firstNumber);
        System.out.println("Second Number : " + secondNumber);

        // Display the results of arithmetic operations
        System.out.println("Addition       : " + addition);
        System.out.println("Subtraction    : " + subtraction);
        System.out.println("Multiplication : " + multiplication);
        System.out.println("Division       : " + division);

    } // End of main method

} // End of BasicCalculatorHardCoding class
