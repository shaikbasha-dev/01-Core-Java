// Square of an Integer (Hard Coding) Program in Java

// Define a public class named SquareOfIntegerHardCoding
public class SquareOfIntegerHardCoding {

    // Main method - JVM starts program execution from here
    public static void main(String[] args) {

        // Declare and initialize an integer variable
        int number = 8;

        // Calculate the square of the integer
        int square = number * number;

        // Display the input value
        System.out.println("Number : " + number);

        // Display the calculated square
        System.out.println("Square : " + square);

    } // End of main method

} // End of SquareOfIntegerHardCoding class
